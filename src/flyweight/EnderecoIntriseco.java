package flyweight;

public class EnderecoIntriseco {
	private String cidade;
	private String bairro;
	
	public EnderecoIntriseco(String cidade, String bairro) {
		this.cidade = cidade;
		this.bairro = bairro;
	}

	public String getCidade() {
		return cidade;
	}

	public String getBairro() {
		return bairro;
	}
	
	
	
	
}
